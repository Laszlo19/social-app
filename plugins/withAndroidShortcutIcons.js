function vectorXml(icon) {
  const pathTag =
    icon.mode === 'stroke'
      ? `<path android:strokeColor="${COLOR}" android:strokeWidth="${icon.sw}" android:strokeLineCap="${icon.cap}" android:strokeLineJoin="${icon.join}" android:pathData="${icon.path}" />`
      : `<path android:fillColor="${COLOR}" android:fillType="evenOdd" android:pathData="${icon.path}" />`
  return `<?xml version="1.0" encoding="utf-8"?>
<vector xmlns:android="http://schemas.android.com/apk/res/android"
  android:width="24dp"
  android:height="24dp"
  android:viewportWidth="${icon.vw}"
  android:viewportHeight="${icon.vh}">
  ${pathTag}
</vector>
`
}

module.exports = function withAndroidShortcutIcons(config) {
  return withDangerousMod(config, [
    'android',
    async config => {
      const drawableDir = path.join(
        config.modRequest.platformProjectRoot,
        'app',
        'src',
        'main',
        'res',
        'drawable',
      )
      fs.mkdirSync(drawableDir, {recursive: true})
      for (const [id, icon] of Object.entries(ICONS)) {
        fs.writeFileSync(
          path.join(drawableDir, `shortcut_${id}.xml`),
          vectorXml(icon),
        )
      }
      return config
    },
  ])
}
